- Система берёт заявки, **проигранные за последние две недели**, по которым вы отправляли **КП**.
- Если по позициям из этого КП цена в каталоге **снизилась больше чем на 15%** уже после отправки КП — клиенту автоматически уходит короткое письмо: «по части позиций цена снизилась, можем подготовить актуальное предложение».
- Письмо уходит **один раз на каждое КП** — клиента не заваливаем повторами.

## Что делать вам

Ничего особенного:

- Если клиент **ответит согласием** — в любой форме, хоть «да», хоть «давайте, присылайте» — заявка **сама вернётся к вам в работу**, и вы увидите её в своём пуле с пометкой.
- Если клиент **не ответит или откажется** — заявка останется закрытой, ничего не меняется.

