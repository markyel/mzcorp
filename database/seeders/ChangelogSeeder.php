<?php

namespace Database\Seeders;

use App\Models\ChangelogEntry;
use Illuminate\Database\Seeder;

/**
 * Стартовые записи раздела «Обновления». Идемпотентно (updateOrCreate по
 * заголовку) — повторный запуск не плодит дубли.
 */
class ChangelogSeeder extends Seeder
{
    public function run(): void
    {
        $entries = [
            [
                'title' => 'Переработан алгоритм распределения заявок',
                'excerpt' => 'Новые заявки распределяются плавным миксом 20/40/40: вес менеджера, текущая нагрузка (взвешенная по стадии: КП=0.5, счёт=0.25) и скорость закрытия за 14 дней. Раздача пропорциональная, без всплесков.',
                'body' => <<<'MD'
Мы сделали распределение новых заявок плавным и более справедливым. Теперь это не «по очереди», а **гладкий микс трёх сигналов** в пропорции **20 / 40 / 40**:

- **Вес менеджера — 20%.** Базовая плановая нагрузка (никто не остаётся в нуле).
- **Текущая нагрузка — 40%.** Учитываются активные заявки, но **взвешенно по стадии**: заявка на этапе КП считается за `0.5`, на этапе счёта — за `0.25`. То есть почти закрытые заявки нагружают меньше, чем новые в работе.
- **Скорость закрытия — 40%.** Сколько заявок менеджер закрыл (успех + потеря) за последние **14 дней**. Кто быстрее доводит до результата — получает чуть больший поток.

## Что это даёт

- Раздача идёт **пропорционально**, без всплесков — никто не получает пачку заявок разом.
- Нагрузка распределяется честнее: учитывается не только количество заявок, но и их стадия и реальная динамика закрытий.
- Поток подстраивается под фактическую загрузку и темп команды.

Sticky-привязка к «своему» клиенту и учёт недоступности (отпуск) продолжают работать поверх этого. Если видите, что заявка ушла не туда — сообщите РОПу: пропорции и параметры настраиваются.
MD,
                'is_published' => true,
            ],
        ];

        foreach ($entries as $data) {
            ChangelogEntry::updateOrCreate(
                ['title' => $data['title']],
                [
                    'excerpt' => $data['excerpt'] ?? null,
                    'body' => $data['body'],
                    'is_published' => $data['is_published'],
                    'published_at' => now(),
                ],
            );
        }
    }
}
