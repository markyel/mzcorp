<?php

namespace Database\Seeders;

use App\Models\ChangelogEntry;
use Illuminate\Database\Seeder;

/**
 * Стартовые записи раздела «Обновления». Идемпотентно (updateOrCreate по
 * заголовку) — повторный запуск не плодит дубли.
 */
class ChangelogSeeder extends Seeder
{
    public function run(): void
    {
        $entries = [
            [
                'title' => 'Переработан алгоритм распределения заявок',
                'body' => <<<'MD'
Мы серьёзно обновили то, как входящие заявки распределяются между менеджерами. Цель — чтобы заявки попадали к нужному человеку и нагрузка делилась честно.

## Что изменилось

- **Sticky-привязка к клиенту.** Если клиент уже работал с конкретным менеджером, его новая заявка по возможности уходит к тому же менеджеру — клиент общается с тем, кто знает его историю.
- **Балансировка нагрузки.** Когда sticky-привязки нет, заявка уходит наименее загруженному доступному менеджеру с учётом планового веса нагрузки.
- **Учёт недоступности.** Менеджеры в отпуске/недоступные исключаются из распределения автоматически, с опциональным делегированием.

## Что это даёт

- Меньше «потерянных» заявок и перебросов вручную.
- Равномернее нагрузка по команде.
- Клиент остаётся со «своим» менеджером.

Если видите, что заявка ушла не туда — сообщите РОПу: правила распределения настраиваются.
MD,
                'is_published' => true,
            ],
        ];

        foreach ($entries as $data) {
            ChangelogEntry::updateOrCreate(
                ['title' => $data['title']],
                [
                    'body' => $data['body'],
                    'is_published' => $data['is_published'],
                    'published_at' => now(),
                ],
            );
        }
    }
}
