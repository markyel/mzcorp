<?php

namespace Database\Seeders;

use App\Models\ChangelogEntry;
use Illuminate\Database\Seeder;

/**
 * Стартовые записи раздела «Обновления». Идемпотентно (updateOrCreate по
 * заголовку) — повторный запуск не плодит дубли.
 */
class ChangelogSeeder extends Seeder
{
    public function run(): void
    {
        $entries = [
            [
                'title' => 'Переработан алгоритм распределения заявок',
                'excerpt' => 'Новые заявки теперь раздаются плавно и по справедливости: система смотрит, кто сколько должен брать, кто насколько занят прямо сейчас и кто быстрее закрывает сделки.',
                'body' => <<<'MD'
Раньше новые заявки раздавались просто по очереди. Теперь перед каждой заявкой система выбирает, кому она подойдёт лучше всего — и делает это спокойно, без перекосов.

## Как система выбирает менеджера

Она смотрит на три простые вещи:

- **Сколько вы должны брать.** У каждого менеджера есть своя плановая доля заявок. Никто не остаётся совсем без работы.
- **Насколько вы заняты прямо сейчас.** Чем меньше у вас активных заявок — тем больше новых придёт. При этом заявки, которые почти доведены до конца, считаются «полегче»: та, по которой уже отправлено КП, нагружает примерно вдвое меньше, а та, по которой выставлен счёт — ещё меньше.
- **Как быстро вы закрываете заявки.** Берутся сделки за последние две недели — и успешные, и отказы. Кто активнее доводит дело до результата, тому идёт чуть больше новых заявок.

## Что это вам даёт

- Заявки приходят **постепенно**, а не пачкой сразу.
- Нагрузка делится по-честному: важно не просто число заявок, а на какой они стадии и как у вас в целом идут дела.
- Если вы временно недоступны (например, в отпуске) — заявки на вас не падают.

Ваши постоянные клиенты по-прежнему по возможности попадают именно к вам. Если видите, что заявка ушла не туда — скажите РОПу: настройки можно поправить.
MD,
                'is_published' => true,
            ],
        ];

        foreach ($entries as $data) {
            ChangelogEntry::updateOrCreate(
                ['title' => $data['title']],
                [
                    'excerpt' => $data['excerpt'] ?? null,
                    'body' => $data['body'],
                    'is_published' => $data['is_published'],
                    'published_at' => now(),
                ],
            );
        }
    }
}
