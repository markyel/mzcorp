    /**
     * Нормализовать значение к каноническому из CoarseCategories::ALL.
     *
     * @param string|null $raw значение из n8n
     * @return string|null каноническое значение или null если разбора нет
     */
    public static function normalize(?string $raw): ?string
    {
        if ($raw === null) {
            return null;
        }
        $trimmed = trim($raw);
        if ($trimmed === '') {
            return null;
        }

        // 1. Уже каноническое — возвращаем как есть
        if (CoarseCategories::isValid($trimmed)) {
            return $trimmed;
        }

        // 2. Карта мусор → канон (lowercase ключ)
        $key = mb_strtolower($trimmed);
        if (array_key_exists($key, self::MAPPING)) {
            return self::MAPPING[$key];
        }

        // 3. Неизвестное мусорное → null (не угадываем)
        return null;
    }

    /**
     * Возвращает true, если значение отнормализовалось бы (каноническое
     * или известное мусорное), false — для unknown garbage.
     */
    public static function isKnown(?string $raw): bool
    {
        if ($raw === null || trim($raw) === '') {
            return true; // null входы валидны (не маппим, но и не unknown)
        }
        $trimmed = trim($raw);
        if (CoarseCategories::isValid($trimmed)) {
            return true;
        }
        return array_key_exists(mb_strtolower($trimmed), self::MAPPING);
    }
