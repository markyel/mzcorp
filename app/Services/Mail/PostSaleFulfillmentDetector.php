<?php

namespace App\Services\Mail;

use App\Models\EmailMessage;

/**
 * Pre-classifier: письмо-просьба отгрузить / поставить на комплектацию уже
 * оформленный заказ — это post_sale, а не новая заявка.
 *
 * gpt-4o на терсовых письмах («Прошу поставить на комплектацию», тема
 * «Отгрузка», вложение-zip с платёжкой) систематически ставит client_request
 * («комплектация → запрос ТМЦ», «вложение может быть спецификацией») →
 * плодятся ошибочные заявки с назначением менеджера (тикеты M-2026-2706,
 * M-2026-2762). Промпт это не лечит — модель упорно держит client_request.
 *
 * ВАЖНО (фидбэк по тикету): информации из самого письма достаточно, чтобы
 * понять что это НЕ новая заявка — анализировать вложения и искать оплаченный
 * заказ не нужно. Сигнал — тема «Отгрузка» и/или тело «прошу поставить на
 * комплектацию / отгрузите». Поэтому детектор работает ТОЛЬКО по контенту:
 *
 *   1) тема содержит «отгрузк…» ЛИБО тело содержит лексику отгрузки/
 *      комплектации купленного («на комплектаци…», «отгруз…»);
 *   2) НЕТ признаков НОВОГО запроса — цены/КП или перечня с количествами
 *      (защита от «отгрузите образцы и заодно посчитайте КП на 50 роликов»).
 *
 * Заказ детектор НЕ ищет и письмо ни к чему не привязывает: в архиве могут
 * быть платёжки по разным счетам — угадывать конкретный заказ нельзя. Такое
 * письмо правильно оставить в общем ящике (info@) нетронутым, без заявки.
 */
class PostSaleFulfillmentDetector
{
    /** Лексика отгрузки/комплектации в теме письма. */
    private const SUBJECT_STEMS = ['отгрузк'];

    /** Лексика отгрузки/комплектации купленного в теле письма. */
    private const BODY_STEMS = ['на комплектаци', 'отгруз'];

    /**
     * Признаки НОВОГО запроса (цена/КП/перечень с количествами). Если хоть
     * один найден — это не чистая отгрузка, отдаём решение LLM.
     */
    private const NEW_ORDER_MARKERS = [
        'коммерческое предложение',
        'пришлите кп',
        'нужно кп',
        'нужна кп',
        'дайте кп',
        'посчита',
        'просчита',
        'рассчита',
        'прайс',
        'стоимост',
        'почём',
        'почем',
        'цена',
        'цену',
        'цены',
        'штук',
        // Явный запрос выставить счёт = инициируется продажа → это НОВАЯ
        // заявка, а не отгрузка уже оплаченного заказа. Тикет: «Прошу
        // выставить счёт и поставить на комплектацию: M12243 — 5шт».
        'выставить сч',
        'выставите сч',
        'выставьте сч',
    ];

    /**
     * Лексика «закрывающие документы / реквизиты» — постпродажный документо-
     * оборот по СОСТОЯВШЕЙСЯ сделке. Используется ТОЛЬКО в контексте, где сделка
     * уже подтверждена (ответ в закрытый тред + у клиента есть оплаченный заказ,
     * см. detectDocsOrFulfillment) — в общем inbox эти слова слишком общие.
     */
    private const DOCS_STEMS = [
        'реквизит',
        'закрывающ',       // закрывающие документы
        'упд',
        'счет-фактур', 'счёт-фактур',
        'акт сверки',
        'оригинал документ', 'оригиналы документ',
        'товарн', // товарная накладная / товарно-транспортная
    ];

    /**
     * Расширенный детект для гарда наследования: письмо в ЗАКРЫТЫЙ тред от
     * клиента с недавним оплаченным/выигранным заказом. Ловит и отгрузку
     * (detect), и документооборот по сделке (реквизиты/УПД/акты). Гварды
     * «нет признаков нового заказа» — те же.
     *
     * @return string|null  Причина либо null.
     */
    public function detectDocsOrFulfillment(EmailMessage $message): ?string
    {
        $base = $this->detect($message);
        if ($base !== null) {
            return $base;
        }

        $subject = mb_strtolower((string) $message->subject);
        $body = mb_strtolower((string) $message->body_plain);
        $haystack = $subject . "\n" . $body;

        $matched = null;
        foreach (self::DOCS_STEMS as $stem) {
            if (str_contains($haystack, $stem)) {
                $matched = $stem;
                break;
            }
        }
        if ($matched === null) {
            return null;
        }
        if (preg_match('/\d+\s*шт/u', $haystack) === 1) {
            return null;
        }
        foreach (self::NEW_ORDER_MARKERS as $marker) {
            if (str_contains($haystack, $marker)) {
                return null;
            }
        }
        if ($this->looksLikeInvoiceRequest($haystack)) {
            return null;
        }

        return "документооборот по сделке («{$matched}»), без запроса цены/КП — постпродажное письмо";
    }

    /**
     * Запрос ВЫСТАВИТЬ / ПРИСЛАТЬ счёт на оплату или на количество товара —
     * инициируется продажа, это НОВАЯ заявка, а не отгрузка/документооборот
     * уже оформленного заказа. Ловит «пришлите счёт на 12 …», «счёт на оплату»,
     * «выставите счёт на …». НЕ матчит «счёт-фактуру» (закрывающий документ по
     * состоявшейся сделке — там после «сч[её]т» идёт дефис, а не « на »).
     * Кейс: esc@interlift.su «Пришлите счет на 12 демпферов … Прошу отгрузить».
     */
    private function looksLikeInvoiceRequest(string $haystack): bool
    {
        return preg_match('/сч[её]т\s+на\s+(оплат|\d)/u', $haystack) === 1;
    }

    /**
     * @return string|null  Причина (для лога/reasoning) либо null — паттерн не сработал.
     */
    public function detect(EmailMessage $message): ?string
    {
        $subject = mb_strtolower((string) $message->subject);
        $body = mb_strtolower((string) $message->body_plain);
        $haystack = $subject . "\n" . $body;

        $matchedSignal = null;
        foreach (self::SUBJECT_STEMS as $stem) {
            if (str_contains($subject, $stem)) {
                $matchedSignal = 'тема об отгрузке';
                break;
            }
        }
        if ($matchedSignal === null) {
            foreach (self::BODY_STEMS as $stem) {
                if (str_contains($body, $stem)) {
                    $matchedSignal = 'текст об отгрузке/комплектации';
                    break;
                }
            }
        }
        if ($matchedSignal === null) {
            return null;
        }

        // Перечень с количествами (в т.ч. склеенный с цифрой — «5шт.») —
        // признак НОВОГО заказа, а не чистой отгрузки. str_contains(' шт')
        // такое не ловил (требовал пробел), поэтому отдельная regex-проверка.
        if (preg_match('/\d+\s*шт/u', $haystack) === 1) {
            return null;
        }

        foreach (self::NEW_ORDER_MARKERS as $marker) {
            if (str_contains($haystack, $marker)) {
                return null;
            }
        }
        if ($this->looksLikeInvoiceRequest($haystack)) {
            return null;
        }

        return $matchedSignal . ' уже оформленного заказа, без запроса цены/КП — постпродажное письмо, не новая заявка';
    }
}
