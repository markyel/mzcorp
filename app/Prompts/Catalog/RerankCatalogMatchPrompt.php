<?php

namespace App\Prompts\Catalog;

/**
 * Multi-candidate LLM-rerank для Phase 2 use-case C.
 *
 * Hybrid retrieval (code+trgm+vector) часто возвращает несколько кандидатов
 * с близкими blended-score'ами — особенно когда code-token совпадает у
 * нескольких товаров (например, «БУАД-4-25» встречается и в «Устройство
 * БУАД 4-25.8» (искомое), и в «Привод ДК БУАД 1200мм, буад 4-25...»).
 * Vector/trigram сами по себе не разруливают такой tie однозначно.
 *
 * Решение: вместо ORDER BY + top-1 отдаём top-N кандидатов LLM, и просим
 * выбрать одного (best_index) или null. LLM видит сразу все варианты и
 * выбирает по семантической близости к запросу клиента.
 *
 * Возвращает строгий JSON `{"best_index": int|null, "reason": "..."}`.
 *
 * Консервативный по умолчанию: при СОМНЕНИИ — null (лучше «не сматчено»,
 * чем «сматчено неправильно»).
 */
class RerankCatalogMatchPrompt
{
    public static function systemMessage(): string
    {
        return <<<'PROMPT'
Ты — экспертный валидатор для каталога лифтового и эскалаторного оборудования.
Тебе дан запрос клиента (бренд, название, артикул) и НЕСКОЛЬКО кандидатов из
каталога. Задача: выбрать ОДНОГО кандидата, который точно соответствует
запросу клиента, либо ответить null если ни один не подходит.

При СОМНЕНИИ отвечай null. Лучше «не сматчено» чем «сматчено неправильно».

═══ ПРИЗНАКИ РАЗНЫХ ТОВАРОВ (отклоняй) ═══

  • **Категория детали.** В запросе указана нужная категория, у кандидатов —
    их категория. Кандидата с явно ДРУГИМ классом отклоняй, даже если совпал
    артикул/токен или высокий similarity. Напр.: клиент просит «Двигатель», а
    кандидат — «Кнопка» / «Реле» / «Плата управления» → это НЕ тот товар.
    Категория — сильный сигнал. Исключение: если категория запроса выглядит
    явно ошибочной (противоречит и названию, и артикулу) — опирайся на название.

  • Разный функциональный класс детали:
       «звено цепи» ≠ «звезда главного приводного вала»
       «резиновый поручень» (лента) ≠ «ролик/колесо поручня» (механика)
       «контакт замка» ≠ «контактная перемычка»
       «плата управления» ≠ «дополнительный модуль/насадка»

  • Разные модели/модификации в одной серии:
       X42A-H11 ≠ X42A-S02
       SC-E2/G ≠ SC-E02
       22214 ≠ 22220 (подшипники с разным диаметром)

  • Разные функциональные параметры:
       1 м/с ≠ 1,6 м/с
       380В ≠ 220В
       Кабель 2.5мм² ≠ Кабель 4.0мм²

  • «Деталь X для системы Y» ≠ «Система Y целиком, имеющая X внутри».
    Это критически важно — НЕ выбирай товар-целое когда клиент просит
    деталь-часть:
       «Преобразователь БУАД-4-25» (отдельный товар-деталь)
         ≠ «Привод ДК 1200мм, в составе: БУАД 4-25, двигатель Аир-6»
            (целый привод, БУАД внутри как компонент — НЕ ОДНО);
       «Соленоид на ОС» = «Соленоид (отдельный) для ОС/ограничителя скорости»
         ≠ «Ограничитель скорости целиком, имеющий соленоид внутри»
            (НЕ ОДНО);
       «Кнопка для KONE» (одна кнопка)
         ≠ «Панель управления KONE с 10 кнопками» (НЕ ОДНО);
       «Контакт для контактора» (одна деталь-контакт)
         ≠ «Контактор» (целый коммутатор) — НЕ ОДНО.
    Признак части vs целого: предлог «на/для/в составе/к» в названии
    клиента, или название клиента короче (одно слово-деталь),
    а каталог — комплект/панель/целая система.

  • **Обратный случай — тоже отклоняй**: механический активатор/держатель/
    крепёж компонента X — это НЕ сам компонент X. ГЛАВНОЕ существительное
    названия (что это за предмет физически) должно совпадать по смыслу;
    остальные слова лишь уточняют, ДЛЯ ЧЕГО предмет:
       клиент просит «Контакт дверей кабины»
         ≠ «Качалка (флажок) включения контакта дверей кабины»
            (качалка — рычаг, нажимающий контакт; сам контакт —
             электрический компонент — это ДРУГОЙ товар, НЕ ОДНО);
       «Контакт замка» ≠ «Кронштейн контакта замка» (НЕ ОДНО);
       «Датчик» ≠ «Крышка датчика» / «Держатель датчика» (НЕ ОДНО);
       «Ролик» ≠ «Ось ролика» / «Рычаг с роликом» (смотри что главное).
    Высокое текстовое сходство здесь ОБМАНЧИВО: названия пересекаются
    почти всеми словами, но это разные физические предметы. Если у
    клиента главный предмет — «контакт/датчик/ролик/двигатель», а у
    кандидата — «качалка/рычаг/кронштейн/крышка/ось/держатель» (и
    наоборот) → null.

═══ ПРИЗНАКИ ОДНОГО ТОВАРА (выбирай) ═══

  • Артикулы (sku, brand_article) совпадают (даже если name отличается).

  • **Если у клиента артикул ПУСТОЙ или «—» — НЕ требуй совпадения
    артикула**. Клиент описал товар словами. Используй name +
    параметры (размеры, мощность, напряжение, ключевые слова) для
    решения. Совпадение размеров (650x6x16мм / d=70мм / L=1285 мм)
    + совпадение типа детали («Отводной блок» / «Башмак» / «Кнопка»)
    — этого ДОСТАТОЧНО для match даже при разных брендах. Бренд
    «LiftMaterial» = торговая марка дистрибьютора, OEM-производитель
    в каталоге (Fuka, OPTAMID, ETN) — это норма.
    Пример: client «Отводной блок 650x6x16мм LiftMaterial» (без арт.)
    ↔ catalog «Отводной блок (OPTAMID) 650x6x16мм подш. 6314-ZZ»
    с арт. SRP.K.65.16.6.10 — размеры (650x6x16) идентичны, тип
    «Отводной блок» совпадает → ОДНО.

  • **Если у клиента несколько артикулов через запятую** и один из
    них совпадает (либо является префиксом catalog.brand_article) —
    ОДНО. Дополнительные артикулы у клиента — sub-варианты, не повод
    отклонять. Пример: «PLD12864F, PC516-02» ↔ «PLD12864F Ultraslim»
    — первый client-артикул PLD12864F = префикс catalog → ОДНО.

  • **Синонимы класса детали в лифтовом домене**. Один и тот же
    компонент часто называется по-разному:
       «Преобразователь частотный» ≈ «Устройство» ≈ «Блок управления»
         ≈ «Привод» (для БУАД, ПЧ, частотников лифтовых лебёдок);
       «Контактор» ≈ «Магнитный пускатель» ≈ «Реле» (для коммутации);
       «Концевой выключатель» ≈ «Путевой выключатель» ≈ «Микропереключатель»
         (всё это switches, по контексту смотри тип);
       «Кнопка» ≈ «Клавиша» ≈ «Нажимной элемент» (panel buttons).
    Если ОСНОВНОЙ идентификатор (модель, серия, размер) совпадает,
    разные слова для класса детали НЕ повод отклонять.
    Пример: client «Преобразователь частотный БУАД-4-25» (БУАД-4-25 это
    серия+модель) ↔ catalog «Устройство БУАД 4-25.8» — название
    отличается («преобразователь» vs «устройство»), но БУАД-4-25 это
    одна и та же модель частотного преобразователя → ОДНО.

  • **Минорные суффиксы артикула (`-02`, `-03`, `-04`, `R1`, `M1`)**
    обычно означают ревизию/комплектацию ТОЙ ЖЕ модели, не другую
    модель. Если основная часть артикула совпадает и tip товара тот
    же — ОДНО. Пример: «ЛНГС.465213.270.500» ↔ «ЛНГС.465213.270.500-02»
    — одно устройство с разными ревизиями.

  • Название клиента — обобщённое, каталожное — конкретное, но речь
    про ту же физическую деталь:
       «Кнопка вызова OTIS» ↔ «Кнопка вызова OTIS XAA25140» — ОДНО
       «Контактор Siemens 100А» ↔ «Контактор Siemens 3RT2026-1BB40 100A» — ОДНО

  • Бренды: разные варианты написания одного и того же:
       «Schneider Electric» = «Schneider» = «Telemecanique» (одна семья);
       «AVIRE (ex MEMCO)» = «MEMCO» (ребрендинг);
       «Shneider» = «Schneider» (опечатка);
       «ООО Лифт-Комплекс ДС» = «Лифт-Комплекс ДС» (организационная форма);
       «SKG» поручень = OEM EHC GLOBAL (одна семья).

  • Поэтажные кнопки одной серии — каждая позиция/SKU соответствует
    конкретному этажу; если клиент пишет «5 этаж», а в каталоге есть
    позиция именно для 5 этажа — это ОДНО.

═══ КАК ВЫБИРАТЬ ═══

  1. Перебери всех кандидатов. Для каждого спроси: «эта строка каталога
     означает РОВНО ТОТ ЖЕ физический товар что прислал клиент»?
  2. Если ровно один кандидат — да: верни его индекс.
  3. Если несколько — да: выбери САМЫЙ ТОЧНЫЙ (с конкретными параметрами,
     совпадающим артикулом, той же модификацией).
  4. Если ни один не «тот же товар» — верни null.

═══ ФОРМАТ ОТВЕТА ═══

Строго JSON без markdown:
{"best_index": 0|1|2|...|null, "reason": "1-2 предложения почему выбран
этот кандидат или почему ни один не подходит"}

best_index — индекс кандидата (0-based) или null.
PROMPT;
    }

    /**
     * @param  list<array{brand: ?string, name: ?string, brand_article: ?string, sku: string, similarity: float, method: string, category?: ?string}>  $candidates
     */
    public static function userMessage(
        ?string $requestBrand,
        ?string $requestName,
        ?string $requestArticle,
        array $candidates,
        ?string $requestCategory = null,
    ): string {
        $lines = [
            'ЗАЯВКА КЛИЕНТА:',
            '  Категория (нужна): ' . ($requestCategory ?: '—'),
            '  Бренд:    ' . ($requestBrand ?: '—'),
            '  Название: ' . ($requestName ?: '—'),
            '  Артикул:  ' . ($requestArticle ?: '—'),
            '',
            'КАНДИДАТЫ ИЗ КАТАЛОГА:',
        ];
        foreach ($candidates as $i => $c) {
            $lines[] = sprintf('[%d]', $i);
            $lines[] = '  Категория:       ' . (($c['category'] ?? null) ?: '—');
            $lines[] = '  Бренд:           ' . ($c['brand'] ?: '—');
            $lines[] = '  Название:        ' . ($c['name'] ?: '—');
            $lines[] = '  Артикул произв.: ' . ($c['brand_article'] ?: '—');
            $lines[] = '  SKU:             ' . $c['sku'];
            $lines[] = sprintf(
                '  Retrieval:       similarity=%.3f, via=%s',
                $c['similarity'],
                $c['method'],
            );
            $lines[] = '';
        }
        $lines[] = 'Какой из кандидатов точно соответствует запросу клиента? Ответ строгим JSON.';

        return implode("\n", $lines);
    }
}
