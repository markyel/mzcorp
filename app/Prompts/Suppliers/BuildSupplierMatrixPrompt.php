<?php

namespace App\Prompts\Suppliers;

/**
 * Промпт сборки матрицы ассортимента поставщика (Фаза 3.1, Foundation §4.2).
 * Из свободного текстового описания МАППИТ на НАШУ таксономию — закрытый список
 * категорий (KB EquipmentCategory) и брендов (ManufacturerBrand). Это гарантирует
 * выравнивание словаря: matrix.categories/brands совпадают с тем, чем тегированы
 * позиции заявок/каталога → точный матч без «неохваченных» из-за разных слов
 * («лебёдки» поставщика → наша «Двигатель главного привода»). См.
 * SupplierMatchService, [[catalog-matching]].
 *
 * gpt-4o-mini, JSON-режим.
 */
class BuildSupplierMatrixPrompt
{
    private const MAX_CHARS = 6000;

    /**
     * @param  array<int, string>  $categories  канонические имена наших категорий
     * @param  array<int, string>  $brands      канонические имена наших брендов
     * @return array<int, array{role: string, content: string}>
     */
    public function build(string $description, array $categories, array $brands): array
    {
        $description = mb_substr(trim($description), 0, self::MAX_CHARS);
        $catList = implode("\n", array_map(fn ($c) => '- ' . $c, $categories));
        $brandList = implode(', ', $brands);

        $system = <<<SYS
Ты разбираешь описание ассортимента поставщика лифтового оборудования и
СОПОСТАВЛЯЕШЬ его с НАШЕЙ таксономией. Не выдумывай — опирайся на текст.

НАШИ КАТЕГОРИИ (выбирай ТОЛЬКО из этого списка, дословно):
{$catList}

НАШИ БРЕНДЫ (используй эти написания, если поставщик их возит):
{$brandList}

Задача:
- categories: какие из НАШИХ категорий покрывает поставщик (массив дословных
  названий из списка выше). Маппь синонимы/разговорные слова на наши категории
  («лебёдка/лебёдки» → «Двигатель главного привода»; «частотник/ПЧ» →
  «Частотный преобразователь»; «двери» → подходящие дверные категории).
  Если поставщик заявляет «любые запчасти / широкий ассортимент» без конкретики —
  можно вернуть все релевантные категории. Категории НЕ из списка не добавляй.
- brands: бренды, которые возит поставщик. Предпочитай написания из нашего
  списка; бренды не из списка тоже можно (как в тексте).
- pairs: ТОЛЬКО если бренд явно связан с конкретной нашей категорией — массив
  {brand, category} (category — дословно из нашего списка). Иначе pairs пустой.

Верни СТРОГО JSON:
{"brands": ["..."], "categories": ["<дословно из списка>"], "pairs": [{"brand":"...","category":"<дословно>"}]}
SYS;

        return [
            ['role' => 'system', 'content' => $system],
            ['role' => 'user', 'content' => "Описание ассортимента поставщика:\n\n{$description}"],
        ];
    }
}
