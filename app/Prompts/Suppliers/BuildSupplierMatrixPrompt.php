<?php

namespace App\Prompts\Suppliers;

/**
 * Промпт сборки матрицы ассортимента поставщика (Фаза 3.1, Foundation §4.2).
 * Из свободного текстового описания ассортимента извлекает: какие БРЕНДЫ
 * (производители) возит поставщик, какие КАТЕГОРИИ оборудования/запчастей, и
 * (если в описании явно связано) конкретные пары бренд×категория.
 *
 * Результат используется SupplierMatchService для подбора релевантных
 * поставщиков под позицию заявки (пересечение по бренду И категории).
 * gpt-4o-mini, JSON-режим.
 */
class BuildSupplierMatrixPrompt
{
    private const MAX_CHARS = 6000;

    /**
     * @return array<int, array{role: string, content: string}>
     */
    public function build(string $description): array
    {
        $description = mb_substr(trim($description), 0, self::MAX_CHARS);

        $system = <<<'SYS'
Ты разбираешь описание ассортимента поставщика лифтового оборудования и
запчастей. Извлеки структуру:

- brands: список БРЕНДОВ/производителей, которых возит поставщик (KONE, OTIS,
  Schindler, ЩЛЗ, КМЗ, Wittur, Sigma, и т.п.). Нормализуй очевидные написания,
  но не выдумывай — только то, что есть в тексте.
- categories: список КАТЕГОРИЙ оборудования/запчастей (лебёдка, двери кабины,
  частотный преобразователь, плата управления, ловитель, канат, ролик, и т.п.)
  — обобщённые типы номенклатуры, упомянутые в описании.
- pairs: ТОЛЬКО если в тексте бренд явно связан с конкретной категорией
  («двери Wittur», «лебёдки KONE») — массив {brand, category}. Если связи нет —
  оставь pairs пустым (поставщик покрывает любую комбинацию из brands×categories).

Если описание общее («любые запчасти», «широкий ассортимент») — brands/categories
оставь пустыми или с тем немногим, что названо. Не галлюцинируй.

Верни СТРОГО JSON:
{"brands": ["..."], "categories": ["..."], "pairs": [{"brand":"...","category":"..."}]}
SYS;

        return [
            ['role' => 'system', 'content' => $system],
            ['role' => 'user', 'content' => "Описание ассортимента поставщика:\n\n{$description}"],
        ];
    }
}
