<?php

namespace App\Prompts\Suppliers;

/**
 * Промпт: из тем и фрагментов писем-ЗАПРОСОВ, которые МЫ отправляли поставщику
 * (запрашивали цены/наличие), составить краткое описание ассортимента — какие
 * бренды и типы запчастей он, судя по запросам, поставляет. Используется
 * майнинг-командой suppliers:mine-from-outbound для bootstrap описаний из
 * истории. Дальше SupplierMatrixBuilder маппит описание на наши 36 категорий.
 *
 * gpt-4o-mini. Только из текста — не выдумывать.
 */
class SummarizeAssortmentFromRfqPrompt
{
    /**
     * @return array<int, array{role: string, content: string}>
     */
    public function build(string $aggregatedRfqText): array
    {
        $system = <<<'SYS'
Тебе дан список тем и фрагментов писем-ЗАПРОСОВ, которые МЫ (поставщик лифтовых
запчастей) отправляли одному контрагенту, запрашивая у него цены/наличие на
номенклатуру. По упомянутой номенклатуре составь КРАТКОЕ описание ассортимента
этого поставщика: какие БРЕНДЫ (производители) и какие ТИПЫ запчастей/
оборудования он, судя по нашим запросам, поставляет.

Требования:
- 2–5 предложений, по-русски, деловой стиль.
- Перечисли конкретные бренды и типы номенклатуры, встретившиеся в запросах.
- НЕ выдумывай — опирайся только на текст. Если данных мало — короткое описание.
- Не пиши вступлений вроде «вот описание» — сразу суть.

Верни СТРОГО JSON: {"description": "..."}
SYS;

        return [
            ['role' => 'system', 'content' => $system],
            ['role' => 'user', 'content' => "Темы и фрагменты наших запросов этому поставщику:\n\n" . $aggregatedRfqText],
        ];
    }
}
