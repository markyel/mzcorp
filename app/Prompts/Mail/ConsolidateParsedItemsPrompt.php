<?php

namespace App\Prompts\Mail;

/**
 * Финальный консолидатор позиций для split-пути разбора заявки.
 *
 * Когда заявку разбирают несколькими парсерами (vision по фото +
 * text по телу письма + извлечение из PDF/XLSX), КАЖДЫЙ источник может
 * описать ОДНУ И ТУ ЖЕ позицию по-своему. Механический дедуп
 * (`dedupeWithinList`) склеивает только точные совпадения по артикулу/имени,
 * поэтому одна позиция, описанная в фото без атрибута и в тексте с атрибутом
 * («…с символом 1» vs «…с символом 1, красная подсветка»), остаётся как два
 * элемента. Кейс M-2026-4011: 6 запрошенных кнопок → 11 позиций.
 *
 * Этот промпт собирает из всех кандидатов ОДИН консолидированный список,
 * понимая, что источники описывают одну заявку. Глобально заявленные
 * атрибуты (цвет/подсветка/материал) применяются ко всем позициям, к которым
 * относятся; явно разные товары сохраняются.
 *
 * Output: {"items":[{name,brand,article,qty,unit,invoice_index,note,
 *          length,length_unit,category,source_indexes:[...]}]}
 * source_indexes — индексы исходных кандидатов, из которых собрана позиция
 * (по ним вызывающий код наследует привязку к фото-вложению).
 */
class ConsolidateParsedItemsPrompt
{
    public static function systemMessage(): string
    {
        return <<<'PROMPT'
Ты — консолидатор товарных позиций одной заявки на запчасти для лифтов и
эскалаторов. Несколько парсеров уже извлекли позиции из РАЗНЫХ источников
одного письма (фото, тело письма, вложения). Источники описывают ОДНУ И ТУ ЖЕ
заявку — один и тот же товар мог попасть в список несколько раз с разными
формулировками.

Твоя задача — собрать ЕДИНЫЙ список позиций, который клиент реально хочет
купить, без дублей.

ПРАВИЛА:
1. Если несколько кандидатов описывают ОДНУ позицию (одна и та же деталь,
   просто разными словами или с/без атрибута) — объедини их в ОДНУ позицию.
   Бери самое полное и точное название; артикул/бренд — если он есть хотя бы
   у одного кандидата.
2. Глобальный атрибут, заявленный в письме для всего набора (например
   «подсветка красная», «цвет белый», «нержавейка»), применяй КО ВСЕМ
   позициям, к которым он относится — даже если конкретный парсер его не
   указал. Не плоди отдельную позицию «с атрибутом» и «без атрибута».
3. Явно РАЗНЫЕ товары сохраняй раздельно. Кнопки с разным символом/этажом
   («символ 1» и «символ 3»), разные артикулы, разные направления стрелок —
   это РАЗНЫЕ позиции, НЕ объединяй их.
4. Количество (qty): для объединяемых дублей бери корректное итоговое
   количество (обычно НЕ сумма — это одна и та же позиция, описанная дважды).
   Суммируй только если кандидаты — это явно отдельные строки одной позиции
   с разным qty из одного источника.
5. invoice_index сохраняй из кандидатов (мульти-счёт кейс). По умолчанию 1.
6. НИКОГДА не выдумывай артикулы, бренды, количества или позиции, которых нет
   ни в одном кандидате. Не добавляй и не удаляй реальные товары — только
   объединяй дубли и переноси атрибуты.

В каждой выходной позиции укажи source_indexes — массив index'ов исходных
кандидатов, из которых она собрана.

Верни СТРОГО JSON вида:
{"items":[{"name":"...","brand":null,"article":null,"qty":1,"unit":"шт.","invoice_index":1,"note":null,"length":null,"length_unit":null,"category":null,"source_indexes":[0,6]}]}
Никакого текста вне JSON.
PROMPT;
    }

    /**
     * @param array<int, array{index:int, source:string, name:string, brand:?string, article:?string, qty:float, unit:string, invoice_index:int, note:?string, length:mixed, length_unit:mixed, category:mixed}> $candidates
     */
    public static function userMessage(string $subject, string $body, ?string $linkedUrlsText, array $candidates): string
    {
        $parts = [];

        $subject = trim($subject);
        if ($subject !== '') {
            $parts[] = "ТЕМА ПИСЬМА:\n{$subject}";
        }

        $body = trim($body);
        if ($body !== '') {
            $parts[] = "ТЕЛО ПИСЬМА:\n" . mb_substr($body, 0, 4000);
        }

        if ($linkedUrlsText !== null && trim($linkedUrlsText) !== '') {
            $parts[] = "ВЫЖИМКА ПО ССЫЛКАМ:\n" . mb_substr(trim($linkedUrlsText), 0, 2000);
        }

        // Кандидаты, сгруппированные по источнику, с глобальным index'ом.
        $lines = [];
        $bySource = [];
        foreach ($candidates as $c) {
            $bySource[$c['source'] ?? 'unknown'][] = $c;
        }
        foreach ($bySource as $src => $list) {
            $lines[] = "ИСТОЧНИК {$src}:";
            foreach ($list as $c) {
                $art = ($c['article'] ?? null) !== null && $c['article'] !== '' ? $c['article'] : '—';
                $brand = ($c['brand'] ?? null) !== null && $c['brand'] !== '' ? $c['brand'] : '—';
                $lines[] = sprintf(
                    '  [%d] %s | бренд: %s | артикул: %s | кол-во: %s %s | счёт: %d',
                    $c['index'],
                    $c['name'] ?? '',
                    $brand,
                    $art,
                    rtrim(rtrim((string) ($c['qty'] ?? 1), '0'), '.') ?: (string) ($c['qty'] ?? 1),
                    $c['unit'] ?? 'шт.',
                    $c['invoice_index'] ?? 1,
                );
            }
        }
        $parts[] = "КАНДИДАТЫ (из разных парсеров одной заявки):\n" . implode("\n", $lines);

        $parts[] = 'Собери единый консолидированный список позиций по правилам. '
            . 'Верни JSON с items[] и source_indexes в каждой позиции.';

        return implode("\n\n", $parts);
    }
}
