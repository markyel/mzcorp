<?php

namespace App\Prompts\Mail;

use App\Models\EmailMessage;

/**
 * Промпт классификатора НАШЕГО ИСХОДЯЩЕГО письма поставщику: это запрос
 * расценки/наличия (RFQ — мы просим у поставщика цены/наличие/сроки на список
 * номенклатуры) ИЛИ это ответ контрагенту как КЛИЕНТУ (мы шлём ему КП/счёт/
 * уточнение по его заявке)?
 *
 * Нужен, потому что один и тот же контрагент бывает и поставщиком, и клиентом.
 * Запускается только для исходящих получателю из реестра поставщиков.
 * gpt-4o-mini, JSON-режим. confidence ниже порога → не RFQ (безопасно).
 */
class ClassifySupplierRfqPrompt
{
    private const MAX_BODY_CHARS = 6000;

    /**
     * @return array<int, array{role: string, content: string}>
     */
    public function build(EmailMessage $outbound): array
    {
        $body = (string) ($outbound->body_plain ?: strip_tags((string) $outbound->body_html));
        $body = mb_substr(trim($body), 0, self::MAX_BODY_CHARS);

        $subject = (string) ($outbound->subject ?? '(без темы)');
        $to = collect((array) ($outbound->to_recipients ?? []))
            ->map(fn ($r) => is_array($r) ? ($r['email'] ?? '') : (string) $r)
            ->filter()->implode(', ');

        $system = <<<'SYS'
Ты анализируешь НАШЕ ИСХОДЯЩЕЕ письмо контрагенту. Реши, что это:

is_rfq=true  — ЗАПРОС РАСЦЕНКИ ПОСТАВЩИКУ: МЫ просим у получателя цену /
               наличие / сроки поставки на список номенклатуры (запчасти,
               узлы, артикулы). Признаки: «прошу дать цену/стоимость/наличие/
               срок», «запрос», перечень позиций с артикулами/количеством,
               «нужно для заказа N». Получатель — поставщик, мы — покупатель.

is_rfq=false — это НЕ запрос расценки. Например: мы отвечаем контрагенту как
               КЛИЕНТУ (шлём ему наше КП/счёт/спецификацию на ЕГО заявку),
               пересылка документов, благодарность, организационное письмо,
               переписка не про закуп у него. Здесь МЫ — продавец, он —
               покупатель, ЛИБО письмо вообще не про запрос цен.

Один и тот же контрагент бывает и поставщиком, и клиентом — смотри на СУТЬ
письма, а не только на адрес. Если МЫ просим цену у него на номенклатуру —
true; если МЫ предлагаем/отвечаем ему — false.

Верни СТРОГО JSON:
{"is_rfq": true|false, "confidence": 0.0-1.0, "reasoning": "кратко по-русски"}
Если сомневаешься — is_rfq=false.
SYS;

        $user = "Получатель: {$to}\nТема: {$subject}\n\nТекст письма:\n{$body}";

        return [
            ['role' => 'system', 'content' => $system],
            ['role' => 'user', 'content' => $user],
        ];
    }
}
