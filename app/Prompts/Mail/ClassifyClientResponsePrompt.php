<?php

namespace App\Prompts\Mail;

use App\Enums\RequestStatus;
use App\Models\EmailMessage;
use App\Models\Request;

/**
 * Промпт классификатора клиентских ответов в треде после КП
 * (Foundation §7.2). Дёргается только если письмо привязано к Request
 * в статусе quoted / under_review / postponed_until / awaiting_clarification.
 *
 * 8 типов intent (Foundation §7.2 + inbound_unclear fallback):
 *   • under_review_acknowledgment
 *   • postponement_request (+ извлекаем дату клиента, если есть)
 *   • invoice_request
 *   • decline_with_reason (+ извлекаем reason taxonomy + цитата)
 *   • clarification_response (только если статус был awaiting_clarification)
 *   • additional_items — клиент добавляет позиции к этой же сделке (расширение)
 *   • new_request — отдельная новая заявка в том же треде
 *   • unclear — алерт менеджеру, без auto-перехода
 *
 * Используем gpt-4o-mini — простая классификация, short body.
 * confidence ниже 0.6 → принудительный unclear (защита от ложных).
 */
class ClassifyClientResponsePrompt
{
    private const MAX_BODY_CHARS = 6000;

    /**
     * @return array<int, array{role: string, content: string}>
     */
    public function build(EmailMessage $message, Request $request): array
    {
        // Классифицируем ТОЛЬКО новый текст ответа клиента — процитированную
        // историю срезаем. Иначе LLM приписывает текущему письму намерение из
        // цитаты: клиент пишет «поменяйте доставку», а в цитате его же
        // первичное «Прошу выставить счёт» — и интент уходит в invoice_request
        // (кейс M-2026-4607). cleanInboundReferenceText тут бессилен: body_plain
        // из HTML часто без переносов, а он режет по началам строк.
        $body = (string) ($message->body_plain ?: strip_tags((string) $message->body_html));
        $body = $this->stripQuotedTail(trim($body));
        $body = mb_substr($body, 0, self::MAX_BODY_CHARS);

        $subject = (string) ($message->subject ?? '(без темы)');
        $fromEmail = (string) ($message->from_email ?? '');
        $fromName = (string) ($message->from_name ?? '');

        // Текущие позиции заявки — нужны, чтобы отличить «расширение той же
        // сделки» (additional_items) от «отдельной новой заявки в том же
        // треде» (new_request): новые позиции тематически связаны со старыми
        // и/или клиент явно просит добавить к этому же заказу/КП/счёту.
        $items = $request->relationLoaded('items')
            ? $request->items
            : $request->items()->where('is_active', true)->orderBy('position')->get();
        $positionsBlock = '';
        foreach ($items as $it) {
            if (isset($it->is_active) && ! $it->is_active) {
                continue;
            }
            $positionsBlock .= '- ' . trim(implode(' · ', array_filter([
                $it->parsed_name,
                $it->parsed_brand,
                $it->parsed_article,
            ]))) . "\n";
        }
        if ($positionsBlock === '') {
            $positionsBlock = "(позиций нет)\n";
        }

        $userPrompt = "## КОНТЕКСТ ЗАЯВКИ\n"
            . "Internal code: {$request->internal_code}\n"
            . "Текущий статус: {$request->status->value} ({$request->status->label()})\n"
            . "Клиент: " . ($request->client_name ?: $request->client_email) . "\n"
            . "Тема заявки: " . ((string) ($request->subject ?? '')) . "\n"
            . "Позиции в этой заявке:\n{$positionsBlock}"
            . "\n"
            . "## ВХОДЯЩЕЕ ПИСЬМО\n"
            . "From: {$fromName} <{$fromEmail}>\n"
            . "Subject: {$subject}\n"
            . "Date: " . ($message->sent_at?->toIso8601String() ?? '') . "\n"
            . "\n"
            . "## ТЕКСТ ПИСЬМА\n"
            . ($body !== '' ? $body : '(пустое тело письма)');

        return [
            ['role' => 'system', 'content' => $this->systemPrompt($request->status)],
            ['role' => 'user', 'content' => $userPrompt],
        ];
    }

    /**
     * Срезать процитированную историю письма, оставив только новый текст ответа
     * клиента. Работает ПОСТРОЧНО-НЕЗАВИСИМО (маркеры ищутся внутри строки):
     * body_plain из HTML часто идёт одной строкой без переносов, поэтому
     * line-based EmailTextCleanerService::cleanInboundReferenceText не срабатывает.
     *
     * Гард против bottom-posting: если до самого раннего маркера почти нет
     * текста (< 12 симв) — вероятно цитата с самого верха / ответ под ней,
     * тогда не режем.
     */
    private function stripQuotedTail(string $text): string
    {
        $text = trim($text);
        if ($text === '') {
            return '';
        }

        $markers = [
            '/-{10,}/u',                                                      // длинный разделитель ----
            '/\d{1,2}\.\d{2}\.\d{4}[ ,][^:]{0,60}?(?:пишет|написал(?:а)?|wrote)\s*:/iu', // «16.06.2026 11:46, X пишет:»
            '/\d{1,2}\.\d{2}\.\d{4},\s*\d{1,2}:\d{2},/u',                     // Yandex «16.06.2026, 13:53,»
            '/\bКому\s*:/u',                                                  // Yandex-forward «Кому:»
            '/\b(?:From|От\s+кого)\s*:/u',
            '/\bНачало\s+пересланного\s+сообщения\b/iu',
            '/^\s*>\s?/mu',                                                   // >-цитата
        ];

        $cut = mb_strlen($text);
        foreach ($markers as $re) {
            if (preg_match($re, $text, $m, PREG_OFFSET_CAPTURE)) {
                $charPos = mb_strlen(substr($text, 0, $m[0][1]));
                $cut = min($cut, $charPos);
            }
        }

        if ($cut < 12) {
            return $text;
        }

        return trim(mb_substr($text, 0, $cut));
    }

    private function systemPrompt(RequestStatus $currentStatus): string
    {
        $clarificationHint = $currentStatus === RequestStatus::AwaitingClientClarification
            ? "Текущий статус заявки = awaiting_client_clarification — ожидаем ответ\n"
                . "на наш вопрос. Если клиент содержательно ответил (например прислал\n"
                . "артикул, фото, описание модели) — intent = clarification_response.\n"
            : "Текущий статус заявки НЕ awaiting_client_clarification.\n"
                . "Intent clarification_response в этом случае НЕ выбирай.\n";

        return <<<PROMPT
Ты — классификатор ответов клиентов на наши коммерческие предложения.
Это часть CRM системы MyZip (запасные части лифтового оборудования).

Письмо — это ответ клиента в треде после того, как мы выслали КП или
задали уточняющий вопрос. Твоя задача — определить намерение клиента
из 6 фиксированных типов.

{$clarificationHint}

═══ ТИПЫ INTENT ═══

1. under_review_acknowledgment — «получили, на согласовании, ответ
   через N дней / через месяц / на следующей неделе»
   • «Получили КП, направил на согласование руководству»
   • «Изучаем, ответим до конца недели»
   • «Передал на рассмотрение, дам обратную связь»

2. postponement_request — клиент откладывает решение до явной даты
   • «Перенесите вопрос на следующий квартал»
   • «Вернёмся к этому после Нового года / в феврале»
   • «Пока не актуально, спросим в марте»
   Извлеки suggested_resume_date (ISO 8601) если возможно.

3. invoice_request — клиент ЯВНО согласовал КП и просит ВЫСТАВИТЬ СЧЁТ.
   Обязателен явный сигнал «оформляйте счёт / берём / согласны»:
   • «Принимаем, выставите счёт»
   • «Согласны, направьте счёт на оплату»
   • «Берём, реквизиты во вложении»

   ВАЖНО — это НЕ invoice_request, выбирай unclear (менеджер поправит КП и
   переспросит), если клиент просто ПРАВИТ условия/КП без слов «выставите
   счёт / согласны / берём»:
   • «Поменяйте, пожалуйста, доставку до терминала ТК» (правка доставки)
   • «Укажите самовывоз / другой адрес / другого получателя» (правка условий)
   • «Пересчитайте на 3 шт вместо 5» / «уберите позицию N» (правка состава)
   • «Добавьте наши реквизиты в КП», «исправьте название организации»
   • Подтверждение, что мы ПРАВИЛЬНО опознали позицию, БЕЗ просьбы о счёте:
     «Да, башмак подтверждаю, такой нужен», «Верно, именно эта позиция»,
     «Да, это тот самый артикул» — клиент подтверждает ИДЕНТИФИКАЦИЮ товара,
     а не желание получить счёт.
   • «Вы же уже выставляли КП на эту позицию» — указание на существующее КП.
   Инструкция по доставке/адресу/реквизитам, подтверждение позиции или простое
   «да / подтверждаю / верно / согласен» САМИ ПО СЕБЕ — это уточнение/
   согласование, а НЕ согласие оформить счёт (intent = unclear или
   under_review_acknowledgment). Счёт запрашивают ОТДЕЛЬНОЙ явной фразой со
   словом «счёт» («выставите счёт», «оформляйте счёт», «счёт на оплату»).
   Если в письме И правка/подтверждение, И явное «выставите счёт» — тогда
   invoice_request.

4. decline_with_reason — клиент ОКОНЧАТЕЛЬНО отказывается от заявки,
   БЕЗ интереса к альтернативе. Это терминальное действие — закрываем
   заявку. Извлеки suggested_closed_lost_reason из taxonomy:
   • client_declined_price        — «дорого», «не вписываемся в бюджет», «нашли дешевле»
   • client_declined_timing       — СРОК ПОСТАВКИ товара не устраивает И клиент
                                     отказывается («ваш срок поставки 8 недель нам
                                     не подходит, откажемся»). НЕ путать с жалобой
                                     на НАШУ медлительность в подготовке КП/ответа —
                                     это НЕ отказ (см. ниже).
   • client_declined_competitor   — «нашли у конкурентов», «уже заказали у X»
   • client_declined_other        — иной отказ
   Также извлеки cited_phrase — ТОЧНАЯ цитата из письма (1-2 предложения),
   подтверждающая отказ. Используется в аналитике дашборда.

   ВАЖНО — это НЕ decline, выбирай unclear (менеджер сам отреагирует):
   • Технический mismatch + клиент даёт СВОИ параметры — это просьба
     скорректировать предложение, не отказ:
     ▸ «Не подходит, у нас 928 мм, а ваша 1128» (указал свой размер)
     ▸ «Нам нужен KM12345 вместо KM67890» (другой артикул)
     ▸ «Эта модель Otis, а у нас KONE» (другой бренд)
     ▸ «Слишком короткий, нужно длиннее» (просит другой вариант)
   • Запрос альтернативы / переговоры:
     ▸ «А есть подешевле / другой производитель / аналог?»
     ▸ «Можно скидку?»
     ▸ «Не могли бы прислать вариант с другими характеристиками?»
   • Указание на ошибку в КП:
     ▸ «Вы прислали не то, что мы запрашивали»
     ▸ «В КП ошибка в количестве/артикуле»
   • ЖАЛОБА / недовольство НАШЕЙ работой (скоростью, сервисом) БЕЗ отказа —
     клиент раздражён, но сделку НЕ отменяет. Это вовлечённый клиент, а не
     потерянный; письмо — сигнал СРОЧНО ответить, а не закрыть заявку:
     ▸ «Сроки выдачи КП просто запредельные!!!» (жалоба на НАШУ медлительность)
     ▸ «Сколько можно ждать счёт?» / «Почему так долго готовите?»
     ▸ «Это уже не смешно, где предложение?»
     Ключ: «сроки выдачи/подготовки КП», «долго отвечаете», «когда пришлёте» —
     это претензия к НАМ, а не отказ от сделки. Особенно если клиент недавно
     просил счёт/КП — он ждёт, а не уходит.

   Правило отличия: если клиент даёт нам ШАНС (другую спецификацию, цену,
   альтернативу) ИЛИ просто ругается на нашу скорость/сервис — это
   negotiation/жалоба, intent = unclear. Только явное «спасибо, не нужно /
   отказываемся / закрываем вопрос / нашли в другом месте» — это decline.

5. clarification_response — клиент ответил на наш уточняющий вопрос
   (статус awaiting_client_clarification). Прислал артикул / фото / модель.
   Если статус другой — НЕ выбирай.

6. unclear — намерение непонятно или письмо вообще не про заявку
   (out-of-office, благодарность, риторический вопрос, общая переписка).
   Менеджеру покажем алерт, статус не двигаем.

7. additional_items — клиент просит ДОБАВИТЬ новые позиции к ЭТОЙ ЖЕ сделке
   (расширение текущей заявки). Признаки: новые позиции тематически связаны
   с уже имеющимися (то же оборудование/объект/бренд), и/или клиент явно
   хочет ОБЩЕЕ предложение/счёт на всё вместе.
   • «Добавьте ещё, пожалуйста, вот эту деталь к заказу»
   • «И ещё нужен <позиция> — посчитайте вместе с предыдущим»
   • «К той же заявке добавьте 5 шт <артикул>, счёт хочу на всё одним»
   Заявку вернём «в работу» (in_progress), менеджер пересоберёт КП/счёт.

8. new_request — в письме СОВЕРШЕННО НОВАЯ заявка, не связанная с текущей,
   просто отправленная в том же треде (клиент ответил на старое письмо, но
   спрашивает про другое оборудование/объект). Признаки: новые позиции НЕ
   связаны с текущими, нет указания «к этому же заказу», по сути это новый
   запрос. Создадим ОТДЕЛЬНУЮ заявку, текущую не трогаем.
   • «Здравствуйте! А ещё подскажите по другому объекту: нужны <совсем другие позиции>»
   • «Отдельный вопрос, не по этому КП: пришлите цены на <другое>»

   Отличие 7 vs 8: 7 — РАСШИРЕНИЕ той же сделки (общий КП/счёт, связанные
   позиции); 8 — НЕЗАВИСИМЫЙ новый запрос. Если сомневаешься и позиции явно
   из другой темы — склоняйся к new_request только при высокой уверенности,
   иначе additional_items (расширение безопаснее — обратимо разъединением).

═══ ПРАВИЛА ═══

• Confidence < 0.6 → принудительно меняй intent на unclear.
• Если письмо одновременно «получили КП» И «дайте счёт» — приоритет
  invoice_request (более конкретное действие).
• «Дорого» без альтернатив — decline_with_reason / client_declined_price.
  «Дорого, есть дешевле?» — НЕ decline, это переговоры → unclear (менеджер
  ответит) или intent decline только если клиент явно отказывается.
• «Подумаем» / «свяжемся» без даты → under_review_acknowledgment.
• Отказ = клиент ЯВНО прекращает сделку («отказываемся», «не берём»,
  «закрывайте», «нашли в другом месте»). Эмоциональная жалоба, ругань на
  сроки/сервис, недовольство БЕЗ явного «не берём» — это НЕ отказ, а
  недовольный, но живой клиент → unclear. Закрывать заявку по жалобе нельзя.

