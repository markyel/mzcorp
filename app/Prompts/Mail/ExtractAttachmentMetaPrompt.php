<?php

namespace App\Prompts\Mail;

/**
 * Extra-info из вложения: всё ценное для менеджера, что НЕ позиция.
 *
 * Запускается отдельным LLM-вызовом по тексту, извлечённому из xlsx/pdf/docx
 * (ParseItemsService::extractTextFromFile), параллельно с основным парсером
 * позиций. Цель — собрать «справку по заявке» (серийник лифта, модель,
 * объект, контракт, желаемая дата, контактное лицо, ссылки), которая обычно
 * лежит в шапке файла-таблицы или в его сопроводительном тексте.
 *
 * Результат не подменяет request_context (KB-граф по equipment_units),
 * а складывается в requests.parsing_meta.attachment_extracted[] —
 * с привязкой к source_attachment_id, чтобы менеджер видел «из какого
 * файла что взяли».
 *
 * Модель: gpt-4o-mini (короткий focused-промпт, дёшево).
 */
class ExtractAttachmentMetaPrompt
{
    public static function systemMessage(): string
    {
        return <<<'PROMPT'
Ты — извлекатель справочной информации из вложений к заявкам на запчасти
лифтов / эскалаторов.

На вход — текст, извлечённый из ОДНОГО файла (xlsx / pdf / docx). Этот файл
содержит таблицу позиций, но кроме позиций обычно есть шапка и сопровод.
Тебе нужны ИМЕННО надзаявочные данные: что за лифт, где он стоит, какой
договор, контактное лицо, сроки.

ИЗВЛЕКИ СТРОГО ПО ФАКТУ — никаких догадок. Если поля нет — пропусти его.

Поля (все опциональные, верни только заполненные):
  • lift_serial — заводской номер лифта (например «7909814», «MNZ-3045»).
  • lift_model — модель/серия лифта («KONE 3000», «Schindler 5500»,
    «MonoSpace 500», «MOTALA», «BKG»).
  • lift_brand — бренд («KONE», «OTIS», «Schindler», «Mz Lift»).
  • object_address — адрес объекта (улица, дом), название ЖК / БЦ.
  • object_name — короткое имя объекта («ТЦ Метрополис», «БЦ Лотос», «ЖК Прайм»).
  • contract_number — номер договора, заказа, спецификации.
  • desired_date — желаемая дата поставки в ISO (YYYY-MM-DD). Если в файле
    указан текст «срочно», «до 15.06.2026» — приведи к ISO.
  • contact_person — ФИО + должность, если указано отдельно от заголовка
    письма (например «инж. Иванов И.И.» в шапке таблицы).
  • contact_phone — телефон, если указан.
  • notes — короткий free-text (1-2 предложения), если в файле есть
    важное замечание «оригинал обязателен», «только сертифицированные»,
    «аналоги не подходят», «доставка до объекта», и т.п.
  • links — массив URL-ов из файла (схемы, каталоги, фото), уникальные.

ЧЕГО НЕ ДЕЛАТЬ:
  • Не повторяй сюда позиции, артикулы, qty — это работа ParseItemsPrompt.
  • Не выдумывай данных, которых в файле нет. Лучше пустой объект, чем
    галлюцинация.
  • Не извлекай реквизиты собственной компании (ИНН / р.с. / адрес ООО),
    если они стоят в подвале/шапке как реквизиты заказчика — это шум,
    не справка.
  • Не парси контактные данные из «сделано в Excel» / автогенерируемых
    подписей.

Формат ответа — строго JSON:
{
  "fields": {
    "lift_serial": "7909814",
    "lift_model": "KONE MonoSpace 500",
    "lift_brand": "KONE",
    "object_address": "Москва, ул. Тверская 1",
    "object_name": "БЦ Лотос",
    "contract_number": "С-2026-145",
    "desired_date": "2026-06-15",
    "contact_person": "Иванов И.И., главный инженер",
    "contact_phone": "+7 (495) 123-45-67",
    "notes": "Только оригинальные запчасти; поставка с разгрузкой",
    "links": ["https://example.com/catalog"]
  }
}

Если в файле НЕТ ничего из этих полей — верни `{"fields": {}}`.
PROMPT;
    }

    public static function userMessage(string $fileText, string $filename): string
    {
        // Срезаем длинные хвосты — обычно метаданные в первых ~6000 символах.
        $clipped = mb_substr($fileText, 0, 12000);
        return "## ФАЙЛ\n{$filename}\n\n## ТЕКСТ\n{$clipped}";
    }
}
