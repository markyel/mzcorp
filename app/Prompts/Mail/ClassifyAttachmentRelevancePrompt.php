<?php

namespace App\Prompts\Mail;

/**
 * Лёгкий классификатор вложения: содержит ли файл ТОВАРНУЮ НОМЕНКЛАТУРУ
 * (перечень запчастей/позиций для КП) — или это служебный документ,
 * не имеющий отношения к составу заявки.
 *
 * Зачем: в pipeline разбора заявки (RequestItemParsingService) любой
 * «тяжёлый» PDF/XLS отключает unified-проход и заставляет идти split-путём,
 * где photo-парсер и text-парсер выдают перекрывающиеся наборы одной и той
 * же заявки → дубли позиций. Классический кейс M-2026-4011: клиент приложил
 * фото кнопок + «реквизиты АЛЬФА-БАНК.pdf». Банковские реквизиты не несут
 * номенклатуры, но рубили unified → 11 позиций вместо 6.
 *
 * Применяется ТОЛЬКО к heavy structured (pdf/xls/xlsx). Fail-soft на стороне
 * вызова: при любой ошибке / неуверенности вложение считается номенклатурным
 * (не теряем возможные позиции).
 *
 * Документы, которые НЕ являются номенклатурой: банковские реквизиты,
 * счета на оплату (платёжные, без перечня товаров для подбора), карточки
 * предприятия, договоры, акты, сертификаты, паспорта качества, доверенности,
 * презентации, прайс-листы поставщика «вообще» (не относящиеся к запросу).
 */
class ClassifyAttachmentRelevancePrompt
{
    public static function systemMessage(): string
    {
        return <<<'PROMPT'
Ты — классификатор вложений входящих писем поставщику запчастей для лифтов
и эскалаторов. На вход — имя файла и первые ~1500 символов извлечённого
текста. Твоя задача — решить, содержит ли вложение ТОВАРНУЮ НОМЕНКЛАТУРУ:
перечень конкретных деталей/позиций, которые клиент хочет купить (название,
артикул, количество, спецификация).

Верни СТРОГО JSON:
{"is_nomenclature": true|false, "doc_type": "краткий тип документа", "reason": "1 фраза"}

is_nomenclature = true — если в документе есть список товарных позиций/запчастей
к запросу (заявка, спецификация, перечень, таблица позиций, выгрузка из 1С со
списком деталей, дефектная ведомость с номенклатурой).

is_nomenclature = false — служебные документы БЕЗ перечня товаров для подбора:
  • банковские реквизиты / карточка предприятия;
  • счёт на ОПЛАТУ как платёжный документ (без задачи подобрать товар);
  • договор, доп. соглашение, акт, УПД;
  • сертификат, декларация, паспорт качества, руководство;
  • доверенность, гарантийное письмо, презентация, логотип, подпись/печать.

Сомневаешься или текст пустой/нечитаемый — отвечай is_nomenclature: true
(лучше лишний раз разобрать, чем потерять позиции). НИКОГДА не отвечай ничем,
кроме JSON.
PROMPT;
    }

    public static function userMessage(string $filename, string $textHead): string
    {
        $filename = trim($filename) !== '' ? $filename : '(без имени)';

        return "Имя файла: {$filename}\n\n"
            . "Начало текста вложения:\n---\n{$textHead}\n---";
    }
}
