<?php

namespace App\Prompts\Request;

use App\Models\EmailMessage;
use App\Models\Request as RequestModel;

/**
 * Финальный валидатор перед автозакрытием заявки без позиций.
 *
 * Контекст: `RequestsRecoverUnassignedCommand` обнаружил Request:
 *   - assigned_user_id IS NULL
 *   - status = Pending
 *   - 0 активных позиций (парсер ничего не нашёл из тела+вложений+Vision)
 *   - возраст ≥ threshold (default 2 часа)
 *
 * До этого промпта мы безусловно закрывали такие заявки с reason=ParserNoContent.
 * Это рождало поток ложно-закрытых заявок, среди которых попадались реальные
 * запросы клиентов (Vision не справился, текст в нестандартном формате,
 * скан, и т.п.).
 *
 * Этот промпт — последний рубеж: LLM смотрит на письмо целиком (тема + тело +
 * имена вложений + отправитель) и решает one of two:
 *
 *   - **close**: письмо явно НЕ содержит заявки или информации для её сборки
 *     (короткий ответ типа «спасибо», «получили», техническое подтверждение,
 *     bounce-уведомление, пустое тело). Безопасно автозакрыть.
 *
 *   - **keep**: письмо ВЫГЛЯДИТ как запрос (есть просьба, упоминаются
 *     товары/детали/количества/бренды), но парсер не справился. Нужно
 *     отдать менеджеру для ручной обработки (autoAssign).
 *
 * Модель: gpt-4o-mini (дёшево, ~2-3к токенов, задача бинарная).
 */
class AutoCloseDecisionPrompt
{
    private const MAX_BODY_CHARS = 4000;
    private const MAX_ATTACHMENTS_LISTED = 15;

    /**
     * @return array<int, array{role: string, content: string}>
     */
    public function build(RequestModel $request, EmailMessage $email): array
    {
        $body = (string) ($email->body_plain ?: strip_tags((string) $email->body_html));
        $body = trim($body);
        $body = mb_substr($body, 0, self::MAX_BODY_CHARS);
        if ($body === '') {
            $body = '(пустое тело)';
        }

        $attachments = $email->relationLoaded('attachments')
            ? $email->attachments
            : $email->attachments()->take(self::MAX_ATTACHMENTS_LISTED + 1)->get();

        $attLines = [];
        foreach ($attachments->take(self::MAX_ATTACHMENTS_LISTED) as $att) {
            $attLines[] = sprintf(
                '- %s (%s, %s байт)',
                $att->filename ?? '(без имени)',
                $att->mime_type ?? '?',
                $att->size_bytes ?? '?',
            );
        }
        if ($attachments->count() > self::MAX_ATTACHMENTS_LISTED) {
            $attLines[] = '… и ещё ' . ($attachments->count() - self::MAX_ATTACHMENTS_LISTED) . ' файлов';
        }
        $attBlock = $attLines === [] ? '(вложений нет)' : implode("\n", $attLines);

        $category = (string) ($email->category ?? '—');
        $intent = (string) ($email->category_intent ?? '—');
        $age = $request->created_at?->diffForHumans(now(), ['parts' => 2, 'short' => true]) ?? '—';

        $userPrompt = "## КОНТЕКСТ\n"
            . "Внутренний код: {$request->internal_code}\n"
            . "Возраст: {$age}\n"
            . "Категория письма (gpt-4o): {$category}\n"
            . "Intent: {$intent}\n"
            . "Парсер позиций нашёл: 0 (ничего)\n"
            . "Linker привязал к существующей заявке: НЕТ\n"
            . "\n"
            . "## ПИСЬМО\n"
            . "От: " . ($email->from_email ?? '—') . " (" . ($email->from_name ?? '') . ")\n"
            . "Тема: " . ($email->subject ?: '(без темы)') . "\n"
            . "\n"
            . "Тело:\n{$body}\n"
            . "\n"
            . "Вложения:\n{$attBlock}\n"
            . "\n"
            . "## ВОПРОС\n"
            . "Содержит ли это письмо явный запрос на товары/услуги, который "
            . "должен попасть в работу менеджеру? Или это «пустышка» (подтверждение, "
            . "благодарность, тех-уведомление, пустое тело, bounce, реклама, "
            . "общая переписка без конкретики) которую безопасно автозакрыть?";

        return [
            ['role' => 'system', 'content' => $this->systemPrompt()],
            ['role' => 'user', 'content' => $userPrompt],
        ];
    }

    private function systemPrompt(): string
    {
        return <<<'PROMPT'
Ты — финальный валидатор перед автозакрытием заявки в B2B CRM для запчастей
лифтов. Видишь письмо клиента, по которому наш парсер не смог извлечь
позиции, и решаешь — закрыть автоматически («нет данных») или оставить
менеджеру на ручную обработку («похоже на запрос»).

Решение:

**verdict=close** (безопасно автозакрыть) если письмо ОЧЕВИДНО НЕ содержит
запроса:
- Короткие ответы-реакции: «спасибо», «получили», «принято», «ок».
- Подтверждения отправки/получения документов без новой просьбы.
- Bounce / NDR / автоответы серверов («delivery failed», «out of office»).
- Чистая благодарность / извинение / прощание без вопросов.
- Тело пустое или почти пустое + нет вложений с осмысленными именами.
- Внутренние пересылки/уведомления систем (трекинги, EDI, ЭДО).
- Маркетинговая рассылка / спам, проскочивший через категоризатор.

**verdict=keep** (отдать менеджеру) если есть ХОТЯ БЫ ОДИН сильный сигнал
запроса:
- Упоминание товаров: артикулы (буквы+цифры), бренды лифтов (KONE, OTIS,
  Schindler, ЩЛЗ, ЛП-, Sigma, Wittur, Mitsubishi, Hyundai и др.), названия
  деталей (плата, кнопка, частотник, концевик, датчик, ремень тяговый,
  канат, шкив и т.п.).
- Количества + единицы («2 шт», «10 м», «100 кг», «комплект»).
- Прямая просьба: «нужны», «нужно», «есть в наличии?», «КП на…», «уточните
  цену», «прошу выставить счёт», «можно ли заказать», «когда отгрузите».
- Описание поломки/проблемы с просьбой найти запчасть.
- PDF/Excel/JPG-вложения с именами, явно указывающими на запрос: «Заявка»,
  «Запрос», «Прайс», «Спецификация», «Заказ», «Перечень», «КП», «Список»,
  «спецификации», артикулы в имени файла.
- Скан/фото без распознанного текста, но с осмысленным именем (`шильдик.jpg`,
  `плата.jpg`) — Vision мог промахнуться, но это явно запрос.

**Принцип сомнения:** если НЕ уверен — лучше keep. Менеджер потратит 30
секунд на «нет, это не заявка», но потерянная реальная заявка стоит
гораздо дороже. Порог: keep если уверенность что это запрос ≥ 0.5.

**Эмпирика confidence в ответе:**
- 0.9+ для verdict=close — явная пустышка (тело <100 симв., пустые вложения,
  bounce, односложный ответ).
- 0.7–0.9 для verdict=close — короткое подтверждение без конкретики, имена
  вложений нейтральные (image001.png, signature.gif).
- 0.6–0.8 для verdict=keep — есть один-два сигнала запроса, но слабые.
- 0.9+ для verdict=keep — явный запрос (артикул + количество + просьба).

ФОРМАТ ОТВЕТА — строго JSON без markdown:

{
  "verdict": "close" | "keep",
  "confidence": 0.0-1.0,
  "reasoning": "1-2 предложения на русском с указанием конкретных сигналов из письма"
}
PROMPT;
    }
}
